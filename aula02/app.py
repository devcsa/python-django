# Operadores Aritméticos
a = 5
b = 3

# Soma
print("Soma:", a + b)

# Subtração
print("Subtração:", a - b)

# Multiplicação
print("Multiplicação:", a * b)

# Divisão
print("Divisão:", a / b)

# Divisão Inteira
print("Divisão Inteira:", a // b)

# Módulo (resto da divisão)
print("Módulo:", a % b)

# Exponenciação
print("Exponenciação:", a ** b)

# Operador de Atribuição
# Atribuição simples
x = 5
print("Atribuição:", x)

# Atribuição com adição
x += 5 # Equivalente a x = x + 5
print("Atribuição com Adição:", x)

# Atribuição com subtração
x -= 5 # Equivalente a x = x - 5
print("Atribuição com Subtração:", x)

# Atribuição com multiplicação
x *= 5 # Equivalente a x = x * 5
print("Atribuição com Multiplicação:", x)

# Atribuição com divisão
x /= 5 # Equivalente a x = x / 5
print("Atribuição com Divisão:", x)

# Operador de Comparação
a = 10
b = 5

# Igual a
print("Igual a:", a == b)

# Diferente de
print("Diferente de:", a != b)

# Maior que
print("Maior que:", a > b)

# Menor que
print("Menor que:", a < b) 

# Maior ou igual a 
print("Maior ou igual a:", a >= b)

# Menor ou igual a
print("Menor ou igual a:", a <= b)

# Operadores
a = True
b = False

# Operador AND
print("AND:", a and b)

# Operador OR
print("OR:", a or b)

# Operador NOT
print("NOT:", not a)