# String
nome_cliente = "Cesar"
print("Nome:", nome_cliente)

# Inteiro
idade_cliente = 18
print("Idade:", idade_cliente)

# Float
altura_cliente = 1.81
print("Altura:", altura_cliente)

# Boolean
situacao = True
print("Situação:", situacao)
situacao = False
print("Situação:", situacao)

# Lista
numeros = [2, 3, 5, 7, 11]
print("Numeros:", numeros)
print("Número:", numeros[2])

# Dicionários
usuario = {
    "nome": "Cesar",
    "idade": 18,
    "cidade": "Cuiabá"
}
print("Nome:", usuario["nome"])
print("Idade:", usuario["idade"])
print("Cidade:", usuario["cidade"])

# Tuplas
curso = ("Curso de Python e Django", "Cesar", 2024, 197.52)
print("Título:", curso[0])
print("Autor:", curso[1])
print("Ano de Publicação:", curso[2])
print("Preço:", curso[3])